class Meetup {
  final String id;
  final String location;
  final String dateTime;
  final String image;
  final String aboutMeet;
  final String title;
  final String type;
  final String noOfParticipants;
  final String sourceName;
  final String sourceImage;

  Meetup(
      {this.id,
      this.location,
      this.dateTime,
      this.image,
      this.aboutMeet,
      this.title,
      this.type,
      this.noOfParticipants,
      this.sourceImage,
      this.sourceName});
}
